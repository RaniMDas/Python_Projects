"This is a program to calculate Gross Salary"
basic_salary            = int(input("Enter the basic salary amount :"))
house_rent_allowance    = int(input("Enter the house rent allowance amount :"))
dearness_allowance      = int(input("Enter the dearness allowance :"))

"We are calculating Gross Salary using the formula Gross Salary = basic_salary + house_rent_allowance + dearness_allowance"
gross_salary            = basic_salary + house_rent_allowance + dearness_allowance
print("The Gross Salary of the person is :", gross_salary)

